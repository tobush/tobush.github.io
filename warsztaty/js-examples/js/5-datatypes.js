'use strict';

/* Typ liczbowy */

var liczba = 10;
console.log(liczba);

var liczbaZmiennoprzecinkowa = 3.14;
console.log(liczbaZmiennoprzecinkowa);

/* Typ łańcuchowy - string */

var name = 'Denys';
console.log(name);

var height = 180 + 'cm';
console.log(height);

var exampleString = 'This is Luke\'s light sword';
console.log(exampleString);

/* Typ logiczny - boolean */

var prawda = true;
console.log(prawda);

var check = (3 > 1);
console.log(check);

/* Typy specjalne*/

var unDefined;
console.log(unDefined);

var nth = null;
console.log(nth);

console.log(12 * null);
console.log('test' * null);
console.log(null * null);

/* Połączenie typów*/

console.log('4' + 3);
console.log('4' - 3);
console.log('4' * 3);
console.log('4'/ 0);
console.log(4 / '2');
console.log('4' / '2');